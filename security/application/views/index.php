    <h2 class="h2b">SQL Injection</h2>
    <div class="information-area">
        <h5 class="h5b">
            Tarayıcı üzerinden sunucuya kötü amaçlı girdi göndermesidir.<br>
            Kötü giriş kontrolleri kötü amaçlı SQL sorgularına neden olabilir.<br>
            Veritabanı komutlarının anlamlarını değiştirmek için kod açıklarından faydalanır.
        </h5>
    </div>
</div>
<div class="content-item-small">
    <h2 class="h2b">CSRF | Cross-site Request Forgery</h2>
    <div class="information-area">
        <h5 class="h5b">
            Kötü internet sitesi, iyi web sitesine masum bir kurbanın kimliği ile tarayıcı isteği gönderir.<br>
            Kullanıcıların oturum bilgilerini kötü sunucuya kaldırır.
        </h5>
    </div>
</div>
<div class="content-item-small">
    <h2 class="h2b">XSS | Cross-site scripting</h2>
    <div class="information-area">
        <h5 class="h5b">
            Kötü internet sitesi dürüst bir siteden bilgileri çalan bir masum kurban script'i gönderir.<br>
            Güvenilen kaynağa zararlı script enjekte eder.
        </h5>
    </div>
